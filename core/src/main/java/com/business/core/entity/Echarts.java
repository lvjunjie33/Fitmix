package com.business.core.entity;

import java.util.Collection;
import java.util.List;

/**
 * Created by sin on 2015/6/10 0010.
 * 用户图形报表类
 */
public class Echarts {

    /**
     * 头部
     */
    private Collection<String> legendData;

    /**
     * 实际数据
     * List<List<Object>>
     */
    private List<Object> seriesData;

    /**
     * x 坐标
     */
    private List<String> xAxisData;


    public Collection<String> getLegendData() {
        return legendData;
    }

    public void setLegendData(Collection<String> legendData) {
        this.legendData = legendData;
    }

    public List<Object> getSeriesData() {
        return seriesData;
    }

    public void setSeriesData(List<Object> seriesData) {
        this.seriesData = seriesData;
    }

    public List<String> getxAxisData() {
        return xAxisData;
    }

    public void setxAxisData(List<String> xAxisData) {
        this.xAxisData = xAxisData;
    }
}
