public class BaseExcetion extends RuntimeException {

    public BaseExcetion(Throwable cause) {
        super(cause);
    }

    public BaseExcetion(String message, Throwable cause) {
        super(message, cause);
    }

    public BaseExcetion(String message) {
        super(message);
    }

    public BaseExcetion(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
